from Crypto.Util.number import long_to_bytes
from math import isqrt

n = 6512617643749294302589095438675618776167938720843890101758895197157390356137924323241304471444329557183412977161734627116921570790884877938134980599305707
c1 = 10907285669560468551647268462406489962787063891723057307575398016663082759381321634953803867684535258520696953786905
c2 = 33582698092528772294080029653133275670665396243438255829051457928307809349340703410415434062259818693740005488773964
ct = 2127264588321127239854001986698891905875335763381083235961453034717795577362487952867927688262105005181067989744994023793619461569137294308259815910038249
e = 65537

# 1. Восстанавливаем приближенные значения P и Q
P_approx = isqrt(c1 << 128)
Q_approx = isqrt(c2 << 128)

# 2. b = Q - P, c = n - P*Q
b = Q_approx - P_approx
c_val = n - P_approx * Q_approx

# Решаем a^2 - ab + c = 0
D = b**2 - 4*c_val
a_est = (b + isqrt(D)) // 2

# 3. Ищем точное значение p в расширенном диапазоне
found = False
for delta in range(-20000, 20000):
    p_test = P_approx + a_est + delta
    if p_test > 1 and n % p_test == 0:
        p = p_test
        q = n // p
        found = True
        print(f"\n[+] Найдено p: {p}")
        break

if found:
    phi = (p - 1) * (q - 1)
    d = pow(e, -1, phi)
    m = pow(ct, d, n)
    # В rsa_task.py было 'little', значит переворачиваем байты 
    flag = long_to_bytes(m)[::-1]
    print(f"[!] Flag: {flag.decode(errors='ignore')}")
else:
    print("\n[-] Не удалось найти делитель. Возможно, уравнение требует уточнения.")


